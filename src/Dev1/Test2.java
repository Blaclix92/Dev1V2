    private void goToEmployeePage(String choice) {

        if (choice.matches("Add Employee")) {
            employeeMainCard.show(EmployeeMainCardLayoutPanel, "employee_add_panel");
        } else if (choice.matches("Delete Employee")) {
            employeeMainCard.show(EmployeeMainCardLayoutPanel, "employee_delete_panel");
        } else if (choice.matches("Modify Employee")) {
            employeeMainCard.show(EmployeeMainCardLayoutPanel, "employee_modify_panel");
        }
    }

    private String checkEmployeeComboBox() {

        String choice = EmployeeComboBox.getSelectedItem().toString();
        return choice;
    }

