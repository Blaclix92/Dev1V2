    private void createDisplayMainCardLayout() {
        DisplayMainCardLayoutPanel.getLayout().addLayoutComponent("employee_panel", EmployeePanel);
        DisplayMainCardLayoutPanel.getLayout().addLayoutComponent("project_panel", ProjectPanel);

        mainCard = (CardLayout) DisplayMainCardLayoutPanel.getLayout();
    }

    //Dit veroorzaakt dat je kan omschakelen tussen Employee en PRoject
    private void displayPageController(String page_name) {
        if (page_name.matches("Employee")) {
            mainCard.show(DisplayMainCardLayoutPanel, "employee_panel");
        } else if (page_name.matches("Project")) {
            mainCard.show(DisplayMainCardLayoutPanel, "project_panel");
        }
    }

